    <h2>STATIC STRECHTING</h2>
    <p>As we age, our muscles become inflexible and taut. To combat muscle inflexibility, static stretching is recommended. Benefits of static stretching include enhanced range of motion, stronger joints, better physical performance while playing sports and a decrease in pain throughout the body.

Static stretching might also help a person relax and lower cortisol levels, which can decrease a person's stress levels and also lower a person's overall body fat percentage. Static stretching can be performed before or after exercise to lengthen and improve the body's flexibility. Although stretching might seem like a passive activity, it is wise to warm up before stretching to promote blood flow to the muscles and to reduce the risk of muscle injury. Static stretching can come in the form of toe-touches, head extension and head flexion and also by way of a few yoga poses.</p>
